    <h2>How to use</h2>
    To access the api for any page you just add ".json" to the end of the url. <br>
    For example if you want to access the stat data for "BiffBish" you just use  <br>
    "ecranked.com/users/BiffBish/stats.json" <br>
    and to access the replay stats data you do <br>
    "ecranked.com/replays/{Session ID}.json" <br>
